// 變數指定：byte、short、int、long、float、double
public class Ch1 {
	public static void main (String [] args) {
		byte A = 20 ;  
		short B = 2547 ;
		int C = 1_314_516 ;
		long D = 32156876L ;
		float E = 3.14159f ;
		float F = 2.2_315f ;
		double G = 2.5169 ;
        System.out.println(A+B) ;
        System.out.println(D-C) ;
        System.out.println(E) ;
        System.out.println(F) ;
        System.out.println(G) ;
	}
}
