//運算
public class Ch1 {
	public static void main (String [] args) {
		int A = 155 ;
		int B = 4 ;
		int ans1 = A + B ;
		System.out.println(A + "+" + B +  "=" + ans1) ;
		int ans2 = A - B ;
		System.out.println(A + "-" + B  + "=" + ans2) ;
		int ans3 = A * B ;
		System.out.println(A + "*" + B  + "=" + ans3) ;
		int ans4 = A / B ;
		System.out.println(A + "/" + B +  "=" + ans4) ;
		float ans5 = (float) A / B ;
		System.out.println(A + "/" + B +  "=" + ans5) ;
		double ans6 = (double) A / B ;
		System.out.println(A + "/" + B  + "=" + ans6) ;
		int ans7 = A % B ;
		System.out.println(A + "%" + B  + "=" + ans7) ;
	}
}
